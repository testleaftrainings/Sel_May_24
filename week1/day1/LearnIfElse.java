package week1.day1;

public class LearnIfElse {

	public static void main(String[] args) {

		int number =0;
		
		if(number>0) {
			System.out.println("positive number");
		}else if(number==0) {
		System.out.println("neutral number");	
		}
		
		else {
			System.out.println("negative number");
		}
	}

}
